<h1>Welcome to Svelte!</h1>
<p>
  Visit <a href="https://svelte.dev/">svelte.dev</a> to learn how to build Svelte
  apps.
</p>
<p>
  This is a template for <a href="https://github.com/sveltejs/template"
    >sveltejs/template</a
  >.
</p>
<p>To get started, edit <code>src/App.svelte</code> and save to reload.</p>
<p>
  To create a new component, create a new file in <code>src/components</code>
  and import it in <code>src/App.svelte</code>.
</p>
<p>
  For a tutorial on how to create a Svelte component, visit <a
    href="https://svelte.dev/tutorial/writing-components"
    >svelte.dev/tutorial/writing-components</a
  >.
</p>
<p>
  For a deeper dive into the Svelte framework, check out <a
    href="https://svelte.dev/docs">svelte.dev/docs</a
  >.
</p>

